full_name = "Aleskandre Midelashvili"
result = ""
for char in full_name :
    if full_name == "A":
        result += "a"
    elif full_name == "M":
        result += "m"
    elif full_name == "I":
        result += "i"
    elif full_name == "L":
        result += "l"
    elif full_name == "E":
        result += "e"
    else:
        result += full_name
print(result)
